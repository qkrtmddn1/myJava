public class Exam3 { // 어디서든 접근 가능한 Exam3 클래스를 선언합니다.
    public static void main(String[] args) { // 프로그램의 시작점인 메인 메서드를 선언합니다.
        int sum = 0; // 합계를 누적할 정수형 변수 sum을 선언하고 0으로 초기화합니다.

        for (int i = 100; i <= 300; i++) { // 100부터 300까지 1씩 증가하며 반복하는 반복문입니다.
            if (i % 5 == 0 || i % 7 == 0) { // 현재 숫자(i)가 5의 배수이거나(||) 7의 배수인지 확인하는 조건문입니다.
                sum += i; // 위 조건을 만족하면 해당 숫자(i)를 sum 변수에 누적하여 더합니다.
            } // if 조건문을 종료합니다.
        } // for 반복문을 종료합니다.

        System.out.println(sum); // 반복문 종료 후 최종 누적된 합계(sum)를 출력합니다.
    } // 메인 메서드를 종료합니다.
} // Exam06 클래스를 종료합니다.