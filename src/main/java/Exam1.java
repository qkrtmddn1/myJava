public class Exam1 { // 어디서든 접근 가능한 Exam1 클래스 선언
    public static void main(String[] args) { // 프로그램 실행의 진입점인 main 메서드 선언
        for (int i = 0; i < 4; i++) { // 총 4줄(행)을 출력하기 위해 4회 반복하는 바깥쪽 반복문
            for (int j = 0; j < 3 - i; j++) { // 피라미드 모양을 만들기 위해 별 앞에 공백(3->0개)을 먼저 출력
                System.out.print(" "); // 줄바꿈 없이 띄어쓰기(공백) 출력
            } // 공백 출력 반복문 종료
            for (int k = 0; k < (i * 2) + 1; k++) { // 각 줄마다 1, 3, 5, 7개로 홀수 단위로 늘어나는 별 출력
                System.out.print("*"); // 줄바꿈 없이 별(*) 문자 이어서 출력
            } // 별 출력 반복문 종료
            System.out.println(); // 한 줄의 공백과 별 출력이 끝난 후 다음 줄로 넘어가기 위한 줄바꿈 수행
        } // 4행 출력을 제어하는 바깥쪽 반복문 종료
    } // main 메서드 종료
} // Exam01 클래스 종료